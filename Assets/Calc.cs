using System.Collections;
using System.Collections.Generic;
using UnityEngine;

public class Calc
{
    //整数の足し算を行うメソッド
    public int Add(int a,int b){
        return a + b;
    }
 
    //整数の引き算を行うメソッド
    public int Sub(int a,int b){
        return a - b;
    }
 
    //整数のかけ算を行うメソッド
    public int Mul(int a,int b){
        return a * b;
    }
 
    //整数の割り算を行うメソッド
    public int Div(int a,int b){
        return a / b;
    }

    

}
